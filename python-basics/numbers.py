# Program to add , subtract , multiply and divide two numbers

first_number = int(input("Enter the first number"))
second_number = int(input("Enter the second number"))


sum_numbers = first_number + second_number
difference_numbers = first_number - second_number
product_numbers = first_number * second_number
quotient_numbers = first_number / second_number



print(f"The sum of {first_number} and {second_number} is {sum_numbers}")
print(f"The difference of {first_number} and {second_number} is {difference_numbers}")
print(f"The product of {first_number} and {second_number} is {product_numbers}")
print(f"The quotient of {first_number} and {second_number} is {quotient_numbers}")


